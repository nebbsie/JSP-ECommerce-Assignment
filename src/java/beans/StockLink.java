
package beans;

public class StockLink {
    
    private String storeName;
    private String productName;
    private int stockID;
    private double productCost;
    private int stockLinkID;
    private String productIMG;
    private int productID;
    

    /**
     * @return the storeName
     */
    public String getStoreName() {
        return storeName;
    }

    /**
     * @param storeName the storeName to set
     */
    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    /**
     * @return the productName
     */
    public String getProductName() {
        return productName;
    }

    /**
     * @param productName the productName to set
     */
    public void setProductName(String productName) {
        this.productName = productName;
    }

    /**
     * @return the stockID
     */
    public int getStockID() {
        return stockID;
    }

    /**
     * @param stockID the stockID to set
     */
    public void setStockID(int stockID) {
        this.stockID = stockID;
    }

    /**
     * @return the productCost
     */
    public double getProductCost() {
        return productCost;
    }

    /**
     * @param productCost the productCost to set
     */
    public void setProductCost(double productCost) {
        this.productCost = productCost;
    }

    /**
     * @return the stockLinkID
     */
    public int getStockLinkID() {
        return stockLinkID;
    }

    /**
     * @param stockLinkID the stockLinkID to set
     */
    public void setStockLinkID(int stockLinkID) {
        this.stockLinkID = stockLinkID;
    }

    /**
     * @return the productIMG
     */
    public String getProductIMG() {
        return productIMG;
    }

    /**
     * @param productIMG the productIMG to set
     */
    public void setProductIMG(String productIMG) {
        this.productIMG = productIMG;
    }

    /**
     * @return the productID
     */
    public int getProductID() {
        return productID;
    }

    /**
     * @param productID the productID to set
     */
    public void setProductID(int productID) {
        this.productID = productID;
    }
    
    
    
}
